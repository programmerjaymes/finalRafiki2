export type Locale = 'en' | 'sw';

/** @deprecated Locale is no longer taken from the URL; use cookie + LocaleProvider. */
export function localeFromPathname(pathname: string): Locale {
  const seg = pathname.split('/').filter(Boolean)[0];
  return seg === 'sw' ? 'sw' : 'en';
}

